// Smooth scrolling for navigation links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', function (e) {
    e.preventDefault();
    document.querySelector(this.getAttribute('href')).scrollIntoView({
      behavior: 'smooth'
    });
  });
});

// Fade-in effect for sections
const sections = document.querySelectorAll('.header-container, .project-container, .skills-container, .contact-container');

const fadeInSection = (entries, observer) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      observer.unobserve(entry.target);
    }
  });
};

const sectionObserver = new IntersectionObserver(fadeInSection, {
  root: null,
  threshold: 0.1
});

sections.forEach(section => {
  sectionObserver.observe(section);
});

// Handle resume download
document.querySelector('.nav-link.btn.btn-primary').addEventListener('click', function(e) {
  e.preventDefault();
  const link = document.createElement('a');
  link.href = this.href;
  link.download = this.download;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
});

